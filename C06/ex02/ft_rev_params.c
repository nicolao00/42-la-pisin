#include <unistd.h>

int	main(int ac, char *av[])
{
	int	i;

	if (ac == 1)
		return (-1);
	while (--ac)
	{
		i = 0;
		while (av[ac][i])
			write(1, &av[ac][i++], 1);
		write(1, "\n", 1);
	}
}
