int	ft_str_is_printable(char *str)
{
	int	idx;

	idx = 0;
	while (str[idx] != '\0')
	{
		if (!(str[idx] >= '!' - 1 && str[idx] <= '~'))
			return (0);
		idx++;
	}
	return (1);
}
