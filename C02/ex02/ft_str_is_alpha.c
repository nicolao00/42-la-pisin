int	ft_str_is_alpha(char *str)
{
	int	idx;

	idx = 0;
	while (str[idx] != '\0')
	{
		if (str[idx] < 'A' || 'z' < str[idx])
			return (0);
		if ('Z' < str[idx] && str[idx] < 'a')
			return (0);
		idx++;
	}
	return (1);
}
